// タグなしのデフォルト（2枚）
const DEFAULT_CANDIDATES: string[] = [
  "ご相談ありがとうございます。\nまずは今の状況を整理しながら見ていきますね。",
  "話しにくいことも、そのままで大丈夫です。\n気になることがあれば続けて送ってくださいね。",
];

// ── メイン関数 ──────────────────────────────────────────────────

export function generateReplyCandidates(
  tags: string[] = [],
  concern?: string
): string[] {
  if (!tags || tags.length === 0) {
    return [
      ...DEFAULT_CANDIDATES,
      generateSalesMessage([], concern),
    ];
  }

  // マッチしたタグの補助文をフラット展開し重複除去
  const matched = tags.flatMap((tag) => TAG_REPLY_MAP[tag] ?? []);
  const unique = Array.from(new Set(matched));

  const baseTemplates =
    unique.length > 0 ? unique : DEFAULT_CANDIDATES;

  // 提案文を末尾に追加（ReplyCandidatesPanel で amber バッジ表示）
  return [...baseTemplates, generateSalesMessage(tags, concern)];
}
