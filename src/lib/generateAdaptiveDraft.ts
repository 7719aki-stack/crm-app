// 3案（やさしめ / 標準 / 強め）を返す

export type DraftStrength = "gentle" | "standard" | "strong";

export interface CustomerDraftContext {
  tone:     string;   // 共感 | 背中押し | アップセル | 報告受け | フォロー
  tags:     string[]; // 恋愛・復縁, 片思い・連絡 など
  status?:  string;   // StatusId 文字列
  ageBand?: string;   // "20代以下" | "30〜40代" | "50代以上"
  gender?:  string;   // "男性" | "女性"
  notes?:   string;
}

export interface DraftCandidate {
  label:    string;
  strength: DraftStrength;
  text:     string;
}

// ── ユーティリティ ──────────────────────────────────────────────────────────

/** 配列からランダムに1つ選ぶ */
function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

/**
 * タグリストを使い map から候補文字列配列を取得。
 * タグが複数ある場合は最初にマッチしたものを使う。
 */
function byTag(map: Record<string, string[]>, tags: string[], fallback: string[]): string[] {
  for (const tag of tags) {
    if (map[tag]) return map[tag];
  }
  return fallback;
}

/** ステータス文字列が特定キーワードを含むか */
function statusIs(status: string | undefined, ...keys: string[]): boolean {
  if (!status) return false;
  return keys.some((k) => status.includes(k));
}

