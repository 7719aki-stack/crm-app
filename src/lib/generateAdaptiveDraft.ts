// ── パーツ取得ヘルパー ──────────────────────────────────────────────────────

function getEmpathy(tone: string, tags: string[]): string {
  const toneMap = EMPATHY_BY_TONE[tone] ?? EMPATHY_BY_TONE["共感"];
  const list = byTag(toneMap, tags, toneMap["_default"] ?? EMPATHY_BY_TONE["共感"]["_default"]!);
  return pick(list);
}

function getGreeting(tags: string[]): string {
  const list = byTag(GREETING, tags, GREETING_DEFAULT);
  return pick(list);
}

function getSituation(tags: string[]): string {
  const list = byTag(SITUATION, tags, SITUATION["_default"]!);
  return pick(list);
}

function getOffer(status: string | undefined, strength: DraftStrength): string {
  if (strength === "strong") return pick(OFFER_STRONG);
  if (statusIs(status, "interested", "free", "upsell_proposed", "paid_proposed")) {
    return pick(OFFER_BY_STATUS.interested);
  }
  if (statusIs(status, "purchased", "full_reversal", "destiny", "deep_guided", "repeater")) {
    return pick(OFFER_BY_STATUS.purchased);
  }
  if (statusIs(status, "waiting", "free_sent", "new_reg")) {
    return pick(OFFER_BY_STATUS.waiting);
  }
  return pick(OFFER_BY_STATUS.default);
}

function getClosing(tone: string): string {
  const list = CLOSING[tone] ?? CLOSING["_default"]!;
  return pick(list);
}

